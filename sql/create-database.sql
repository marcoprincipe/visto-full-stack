USE mysql;

-- ########################################################################
-- # Efetua a criação do banco de dados visto_db
-- ########################################################################

CREATE database IF NOT EXISTS visto_db;

-- ########################################################################
-- # Efetua a criação do usuário visto_user
-- ########################################################################

CREATE USER IF NOT EXISTS 'visto_user'@'%' IDENTIFIED BY 'M@pg2E07';

-- ########################################################################
-- # Atribui todas as permissões ao usuário visto_db no banco de dados
-- ########################################################################

GRANT ALL PRIVILEGES ON visto_db.* TO 'visto_user'@'%';

-- ########################################################################
-- # Efetiva a atribuição de permissões
-- ########################################################################

FLUSH PRIVILEGES;